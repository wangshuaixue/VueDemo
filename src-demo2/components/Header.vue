<template>
  <div class="todo-header">
    <input type="text" placeholder="请输入你的任务名称，按回车键确认"
           v-model="inputTodo"  @keyup.enter="add" />
  </div>
</template>
<script>
  export default {  //配置对象
  /*props:{
    addTodo:{
      type:Function,
      required:true
    }
  },*/
  data(){
    return {
       inputTodo:''
    }
  },
  methods:{
    add(){
      const inputTodo=this.inputTodo.trim()
      if(!inputTodo){
        return
      }
      const todo={title:inputTodo,completed:false}
//      this.addTodo(todo)
      //通知父组件，让父组件调用addTodo这个方法
      this.$emit('addTodo',todo) //分发事件名，让父组件调用,如果需要参数的话，后面接着传所需要的参数
      this.inputTodo=''
    },

  }
  }
</script>
